
public  class FlashCard {
	//the object stored in flashcard arraylist
	//uses getters and setters for the String values of Front and Back
	
	private String front;
	private String back;

	public FlashCard(String f, String b) {
		front = f;
		back = b;
	}
	
	public String getFront() {
		return front;
	}

	public void setFront(String front) {
		this.front = front;
	}

	public String getBack() {
		return back;
	}

	public void setBack(String back) {
		this.back = back;
	}


	

}
                                                                                                   